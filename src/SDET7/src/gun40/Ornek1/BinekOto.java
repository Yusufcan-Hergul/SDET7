package gun40.Ornek1;

public abstract class BinekOto {

    private String marka;

    private int uretimYılı;

    private int vitesadedi;

    public String marka() {
        return marka;
    }

    public void setMarka(String marka) {
        this.marka = marka;
    }

    public int uretimYılı() {
        return uretimYılı;
    }

    public void setUretimYılı(int uretimYılı) {
        this.uretimYılı = uretimYılı;
    }

    public int vitesadedi() {
        return vitesadedi;
    }

    public void setVitesadedi(int vitesadedi) {
        this.vitesadedi = vitesadedi;
    }
}
